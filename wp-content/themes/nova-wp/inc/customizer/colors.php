<?php
/**
 * Nova WP colors
 *
 * @package nova-wp
 */

/**
 * Black
 * @return  string color
 */
function nova_wp_color_black( $color ) {
	$color = '#111111';
	return $color;
}
/**
 * Jasper
 * @return  string color
 */
function nova_wp_color_off_white( $color ) {
	$color = '#eeeeee';
	return $color;
}

function nova_wp_color_red( $color ) {
	$color = '#EA3A3C';
	return $color;
}

/**
 * White
 * @return  string color
 */
function nova_wp_color_white( $color ) {
	$color = '#ffffff';
	return $color;
}

/**
 * Charcoal
 * @return  string color
 */
function nova_wp_color_charcoal( $color ) {
	$color = '#444444';
	return $color;
}
